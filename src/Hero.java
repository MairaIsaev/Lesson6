public class Hero {
     String Naruto;
     int health;
     int damage;
     String superpower;

     public String getNaruto() {
          return Naruto;
     }

     public void hero(int health, int damage, String superpower) {
          this.Naruto = Naruto;
          this.health = health;
          this.damage = damage;
          this.superpower = superpower;
     }

     public int getHealth() {
          return health;
     }

     public int getDamage() {
          return damage;
     }

     public String getSuperpower() {
          return superpower;
     }

     public void hero(int health, int damage){
          this.health = health;
          this.health = damage;
     }

}
